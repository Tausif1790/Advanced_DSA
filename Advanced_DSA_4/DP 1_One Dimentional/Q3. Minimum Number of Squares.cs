Given an integer A. Return minimum count of numbers, sum of whose squares is equal to A.
Input 1: A = 6
Input 2: A = 5

Output 1: 3
Output 2: 2

Explanation 1:
 Possible combinations are : (12 + 12 + 12 + 12 + 12 + 12) and (12 + 12 + 22).
 Minimum count of numbers, sum of whose squares is 6 is 3. 

